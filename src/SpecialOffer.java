public class SpecialOffer {
    Product product;
    String offer;
    String timeFrom;
    String timeTo;
    double discount;

    SpecialOffer(Product p, String o, String tF, String tT, double d) {
        product = p;
        offer = o;
        timeFrom = tF;
        timeTo = tT;
        discount = d;

    }

}
