public class SpecialOffer {
    Product product;
    String offer;
    String timeFrom;
    String timeTo;
    double discount;

    SpecialOffer(Product p_product, String p_offer, String p_timeFrom, String p_timeTo, double p_discount) {
        product = p_product;
        offer = p_offer;
        timeFrom = p_timeFrom;
        timeTo = p_timeTo;
        discount = p_discount;

    }

}
